package Practice;

public class Printnumbers100to1 {

	public static void main(String[] args) {

		int i;
		for(i=155;i>=56;i--) {
			
			System.out.println(i-55);
			
		}
	}
}


// i 155  100   55
// i 154   99   55
// i 153   98   55 	
// i 152   97   55

// 155   100 = 55
// 154	  99 = 55
// 153    98 = 55